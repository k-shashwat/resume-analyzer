# =============================================================================
# Build the expansion cache from static map + runtime-parsed acronyms
# =============================================================================

_expansion_cache: Optional[dict[str, set[str]]] = None
_runtime_groups: List[List[str]] = []


def add_runtime_groups(groups: List[List[str]]) -> None:
    """Register acronym groups parsed from a JD at runtime."""
    global _runtime_groups
    _runtime_groups.extend(groups)


def reset_runtime_groups() -> None:
    """Clear runtime groups between analysis sessions."""
    global _runtime_groups, _expansion_cache
    _runtime_groups = []
    _expansion_cache = None


def set_domain(domain: Optional[str]) -> None:
    """Filter the static acronym groups to only those relevant to the given domain."""
    global _expansion_cache, _filtered_groups
    if domain is None or domain == "all":
        _filtered_groups = None
        _expansion_cache = None
        return
    from lib.data.domains import get_acronym_group_indices_for_domain
    indices = get_acronym_group_indices_for_domain(domain)
    if indices is not None:
        _filtered_groups = [ACRONYM_GROUPS[i] for i in indices if i < len(ACRONYM_GROUPS)]
    _expansion_cache = None


_filtered_groups: Optional[List[List[str]]] = None


def _get_active_groups() -> List[List[str]]:
    if _filtered_groups is not None:
        return _filtered_groups + _runtime_groups
    return ACRONYM_GROUPS + _runtime_groups


def _build_expansion_cache() -> dict[str, set[str]]:
    cache: dict[str, set[str]] = {}
    all_groups = _get_active_groups()
    for group in all_groups:
        group_lower: set[str] = {term.lower() for term in group}
        for term in group_lower:
            if term in cache:
                cache[term] |= group_lower
            else:
                cache[term] = group_lower.copy()
    return cache


def expand_terms(terms: set[str]) -> set[str]:
    """Expand a set of terms using both static and runtime acronym maps."""
    global _expansion_cache
    if _expansion_cache is None:
        _expansion_cache = _build_expansion_cache()
    expanded: set[str] = set()
    for term in terms:
        term_lower = term.lower()
        expanded.add(term_lower)
        if term_lower in _expansion_cache:
            expanded |= _expansion_cache[term_lower]
    return expanded
