# =============================================================================
# Layer 1: Runtime JD parenthetical parser
# Extracts "Term (ACRO)" and "ACRO (Term)" definitions from the JD text at
# runtime. This auto-scales to ANY domain without maintaining a static map.
# =============================================================================

_DEF_IN_PARENS = re.compile(r"\b([A-Z][a-zA-Z]*(?:\s+[A-Z][a-zA-Z]*){1,5})\s*\(([A-Z][A-Za-z0-9&/-]{1,9})\)")
_ACRO_IN_PARENS = re.compile(r"\b([A-Z][A-Za-z0-9&/-]{1,9})\s*\(([A-Z][a-zA-Z]*(?:\s+[A-Z][a-zA-Z]*){1,5})\)")
_AFTER_PARENS = re.compile(r"\(([A-Z][A-Za-z0-9&/-]{1,9})\)\s+([A-Z][a-zA-Z]*(?:\s+[A-Z][a-zA-Z]*){1,5})\b")

_STOP_PREFIX_WORDS = {"the", "a", "an", "this", "that", "our", "their",
                       "with", "using", "including", "such", "must", "should",
                       "will", "can", "may", "also", "need", "require"}


def _clean_phrase(raw: str) -> str:
    words = raw.strip().lower().split()
    while words and words[0] in _STOP_PREFIX_WORDS:
        words = words[1:]
    if len(words) < 2:
        return ""
    return " ".join(words)


def parse_acronyms_from_text(text: str) -> List[List[str]]:
    """Extract inline acronym definitions from text.

    Handles three patterns:
      1. Definition (ACRO)  e.g. 'Natural Language Processing (NLP)'
      2. ACRO (Definition)  e.g. 'NLP (Natural Language Processing)'
      3. (ACRO) Definition  e.g. '(NLP) Natural Language Processing'
    """
    groups: List[List[str]] = []
    seen: set[str] = set()

    for phrase, acro in _DEF_IN_PARENS.findall(text):
        phrase_clean = _clean_phrase(phrase)
        acro_clean = acro.strip().lower()
        if phrase_clean and len(acro_clean) >= 2:
            key = frozenset({acro_clean, phrase_clean})
            if key not in seen:
                seen.add(key)
                groups.append([acro_clean, phrase_clean])

    for acro, phrase in _ACRO_IN_PARENS.findall(text):
        phrase_clean = _clean_phrase(phrase)
        acro_clean = acro.strip().lower()
        if phrase_clean and len(acro_clean) >= 2:
            key = frozenset({acro_clean, phrase_clean})
            if key not in seen:
                seen.add(key)
                groups.append([acro_clean, phrase_clean])

    for acro, phrase in _AFTER_PARENS.findall(text):
        phrase_clean = _clean_phrase(phrase)
        acro_clean = acro.strip().lower()
        if phrase_clean and len(acro_clean) >= 2:
            key = frozenset({acro_clean, phrase_clean})
            if key not in seen:
                seen.add(key)
                groups.append([acro_clean, phrase_clean])

    return groups


